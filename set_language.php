<?php
// Устанавливаем сессию, если еще не установлена
session_start();

// Проверяем, получен ли POST-запрос с языком
if (isset($_POST['language'])) {
    // Устанавливаем выбранный язык в сессию
    $_SESSION['language'] = $_POST['language'];
    // Записываем сообщение об изменении языка в журнал ошибок
    error_log("Смена языка на: " . $_SESSION['language']);
    // Отправляем корректный HTTP-ответ (успешно выполнено)
    http_response_code(200);
} else {
    // Отправляем HTTP-ответ о некорректном запросе (ошибка на стороне клиента)
    http_response_code(400);
}
?>
